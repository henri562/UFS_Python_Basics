num1 = int(input('Enter a number:'))
num2 = int(input('Enter another number:'))
if num1 != num2:
    print('num1 is not equal to num2')
    if num1 > num2:
        print('num1 is greater than num2')
    else:
        print('num1 is less than num2')
else:
    print('num1 is equal to num2')


