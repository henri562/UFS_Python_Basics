from turtle import *


def draw_square(size, colour):
    fillcolor(colour)
    begin_fill()
    for i in range(4):
        forward(size)
        left(90)
    end_fill()


draw_square(50, 'black')
penup()
forward(50)
pendown()
draw_square(50, 'white')
penup()
forward(50)
pendown()
